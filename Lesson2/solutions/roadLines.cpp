#include<iostream>
using namespace std;
int main()
{
	// Можем да си представим, че Трайчо изминава един метър, после два, после три и така до n-тия метър 
	// и това става два пъти, защото се връща.
	// По малкия Гаус( аритметична прогресия ) можем да сметнем колко път ще измине Трайчо.
	
	int n, ans;
	
	cin>>n;
	
	// По Малкия Гаус, но умножен по две, защото се връща всеки път
	ans = (n+1)*n;
	
	cout<<ans<<endl;
	return 0;
}